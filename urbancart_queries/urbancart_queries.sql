-- -----------------------------------------------
-- Q1. How many total orders has UrbanCart received so far?
-- -----------------------------------------------
SELECT COUNT(DISTINCT order_id) AS "Total Orders"
FROM public."FactOrderItems";
-- Result: 1,200 total orders [cite: 3-8]

-- -----------------------------------------------
-- Q2. Which cities generate the highest number of orders & revenue?
-- -----------------------------------------------
SELECT c.city AS "City", COUNT(o.order_id) AS "Total Orders", SUM(p.quantity * q.unit_price) AS "Total Revenue"
FROM public."FactOrders" o
JOIN public."DimCustomers" c ON o.customer_id = c.customer_id
JOIN public."FactOrderItems" p ON o.order_id = p.order_id
JOIN public."DimProducts" q ON p.product_id = q.product_id
GROUP BY 1
ORDER BY 2 DESC;
-- Result: Barishal leads [cite: 9-23]

-- -----------------------------------------------
-- Q3. What percent of total customers uses Gmail?
-- -----------------------------------------------
SELECT COUNT(CASE WHEN email ILIKE '%@gmail%' THEN 1 END) * 100 / COUNT(customer_id) AS "Gmail Users (%)"
FROM public."DimCustomers";
-- Result: 70% [cite: 24-31]

-- -----------------------------------------------
-- Q4. What is the monthly trend of total orders over time?
-- -----------------------------------------------
SELECT TO_CHAR(order_date::DATE, 'Month YYYY') AS "Month", COUNT(order_id) AS "Total Orders"
FROM public."FactOrders"
GROUP BY 1
ORDER BY 1 DESC;
-- Result: Oct 2025 peaked at 413 orders [cite: 32-42]

-- -----------------------------------------------
-- Q5. What is the order completion rate across all orders?
-- -----------------------------------------------
SELECT ROUND((COUNT(CASE WHEN status ILIKE 'Completed' THEN 1 END) * 100.0 / COUNT(DISTINCT order_id)), 2) AS "Completion Rate(%)"
FROM public."FactOrders";
-- Result: 59.4% [cite: 43-49]

-- -----------------------------------------------
-- Q6. What is the total revenue generated by UrbanCart?
-- -----------------------------------------------
