-- -----------------------------------------------
-- Q19. City preferences for payment methods.
-- -----------------------------------------------
SELECT p.city AS "City", o.method AS "Payment Method", COUNT(DISTINCT o.payment_id) AS "Transaction Count",
ROUND(COUNT(DISTINCT o.payment_id) * 100.0 / SUM(COUNT(DISTINCT o.payment_id)) OVER (PARTITION BY p.city), 2) AS "City Share (%)"
