-- -----------------------------------------------
-- Q22. Difference between category average price and product price.
-- -----------------------------------------------
SELECT product_name AS "Product Name", category AS "Product Category", unit_price AS "Unit Price",
ROUND(AVG(unit_price) OVER (PARTITION BY category), 2) AS "Category Avg Price",
unit_price - ROUND(AVG(unit_price) OVER (PARTITION BY category), 2) AS "Difference"
FROM public."DimProducts"
ORDER BY 5 ASC;
-- Result: Earphones highest negative diff [cite: 302-317]

-- -----------------------------------------------
-- Q23. Products most frequently purchased together.
-- -----------------------------------------------
SELECT f1.product_id AS "Product 1", f2.product_id AS "Product 2", COUNT(*) AS "Pair Count"
FROM public."FactOrderItems" f1
JOIN public."FactOrderItems" f2 ON f1.order_id = f2.order_id AND f1.product_id < f2.product_id
GROUP BY 1, 2
ORDER BY 3 DESC;
-- Result: Prod 24 & 30 most paired [cite: 318-331]

-- -----------------------------------------------
-- Q24. Product pairs generating highest total revenue.
-- -----------------------------------------------
SELECT f1.product_id AS "Product 1", f2.product_id AS "Product 2",
SUM((p1.quantity * p1.unit_price) + (p2.quantity * p2.unit_price)) AS "Total Revenue"
FROM public."FactOrderItems" f1
JOIN public."FactOrderItems" f2 ON f1.order_id = f2.order_id AND f1.product_id < f2.product_id
JOIN public."DimProducts" p1 ON f1.product_id = p1.product_id
JOIN public."DimProducts" p2 ON f2.product_id = p2.product_id
GROUP BY 1, 2
ORDER BY 3 DESC;
-- Result: Prod 9 & 40 highest [cite: 332-348]

-- -----------------------------------------------
-- Q25. Daily report: Date, Total Orders, Items, Completed, Cancelled, Revenue.
-- -----------------------------------------------
SELECT TO_DATE(o.order_date, 'MM/DD/YYYY') AS "Order Date",
COUNT(DISTINCT o.order_id) AS "Total Orders",
SUM(c.quantity) AS "Total Items",
COUNT(DISTINCT CASE WHEN o.status = 'Completed' THEN o.order_id END) AS "Completed Orders",
COUNT(DISTINCT CASE WHEN o.status = 'Cancelled' THEN o.order_id END) AS "Cancelled Orders",
COUNT(DISTINCT CASE WHEN o.status = 'Pending' THEN o.order_id END) AS "Pending Orders",
SUM(c.quantity * q.unit_price) AS "Total Revenue"
FROM public."FactOrders" o
JOIN public."FactOrderItems" c ON o.order_id = c.order_id
JOIN public."DimProducts" q ON c.product_id = q.product_id
GROUP BY 1
ORDER BY 1;
-- Result: Daily breakdown [cite: 349-353]