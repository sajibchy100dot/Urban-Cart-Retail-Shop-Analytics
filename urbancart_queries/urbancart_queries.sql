JOIN public."FactOrders" c ON o.order_id = c.order_id
JOIN public."DimCustomers" p ON c.customer_id = p.customer_id
GROUP BY 1, 2
ORDER BY 1, 3 DESC;
-- Result: COD preferred all cities [cite: 258-277]

