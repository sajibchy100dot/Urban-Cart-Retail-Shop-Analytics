FROM public."FactOrderItems" o
JOIN public."DimProducts" c ON o.product_id = c.product_id;
-- Result: BDT 22,45,122 [cite: 50-56]

-- -----------------------------------------------
-- Q7. Which product categories contribute the most to total revenue?
