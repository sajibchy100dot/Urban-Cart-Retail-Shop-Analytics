-- -----------------------------------------------
-- Q9. What is the Average Order Value (AOV) and Average Basket Size?
-- -----------------------------------------------
-- Average Order Value
SELECT ROUND(SUM(o.quantity * c.unit_price) / COUNT(DISTINCT order_id), 2) AS "AOV"
FROM public."FactOrderItems" o
JOIN public."DimProducts" c ON o.product_id = c.product_id;

-- Average Basket Size
SELECT ROUND(SUM(quantity) / COUNT(DISTINCT order_id), 2) AS "Average Basket Size"
FROM public."FactOrderItems";
-- Result: AOV = BDT 1,870.94 | Average Basket Size = 9.96 items [cite: 80-93]

-- -----------------------------------------------
-- Q10. Which products are at risk of stock-out?
-- -----------------------------------------------
SELECT product_name AS "Product", category AS "Product Category", stock AS "Stock",
CASE
    WHEN stock <= 100 THEN 'Critical'
    WHEN stock <= 200 THEN 'Low'
    WHEN stock <= 500 THEN 'Medium'
    ELSE 'Healthy'
END AS "Status"
FROM public."DimProducts"
ORDER BY 3 ASC;
-- Result: Power Bank 10000mAh is Critical [cite: 94-111]

-- -----------------------------------------------
-- Q11. Which customers contribute the highest total revenue?
-- -----------------------------------------------
SELECT q.customer_id AS "Customer ID", r.full_name AS "Name", SUM(o.quantity * c.unit_price) AS "Total Revenue"
FROM public."FactOrderItems" o
JOIN public."DimProducts" c ON o.product_id = c.product_id
