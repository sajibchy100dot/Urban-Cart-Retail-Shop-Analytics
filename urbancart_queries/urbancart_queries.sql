-- -----------------------------------------------
-- Q12. Cancellation rate by city and customer.
-- -----------------------------------------------
-- City
SELECT o.city AS "City", COUNT(c.order_id) AS "Total Orders", COUNT(CASE WHEN c.status ILIKE '%cancelled%' THEN 1 END) AS "Cancelled Orders",
ROUND(COUNT(CASE WHEN c.status ILIKE '%cancelled%' THEN 1 END) * 100.0 / COUNT(c.order_id), 2) AS "Cancellation Rate (%)"
FROM public."FactOrders" c
JOIN public."DimCustomers" o ON c.customer_id = o.customer_id
GROUP BY o.city
ORDER BY "Cancellation Rate (%)" DESC;

-- Customer
SELECT o.full_name AS "Customer Name", COUNT(c.order_id) AS "Total Orders", COUNT(CASE WHEN c.status ILIKE '%cancelled%' THEN 1 END) AS "Cancelled Orders",
ROUND(COUNT(CASE WHEN c.status ILIKE '%cancelled%' THEN 1 END) * 100.0 / COUNT(c.order_id), 2) AS "Cancellation Rate (%)"
FROM public."FactOrders" c
JOIN public."DimCustomers" o ON c.customer_id = o.customer_id
GROUP BY o.full_name
ORDER BY "Cancellation Rate (%)" DESC;
-- Result: Gazipur (27%) | Tanvir (50%) [cite: 128-159]

-- -----------------------------------------------
-- Q13. Gender purchasing patterns by category.
-- -----------------------------------------------
SELECT c."Gender", COUNT(DISTINCT o.order_id) AS "Total Orders", SUM(p.unit_price * oi.quantity) AS "Total Revenue (BDT)",
ROUND(AVG(p.unit_price * oi.quantity), 2) AS "Avg Order Value", COUNT(CASE WHEN o.status ILIKE '%cancelled%' THEN 1 END) AS "Cancelled Orders",
ROUND(COUNT(CASE WHEN o.status ILIKE '%cancelled%' THEN 1 END) * 100.0 / COUNT(o.order_id), 2) AS "Cancel Rate (%)"
FROM public."FactOrders" o
JOIN public."DimCustomers" c ON o.customer_id = c.customer_id
JOIN public."FactOrderItems" oi ON o.order_id = oi.order_id
JOIN public."DimProducts" p ON oi.product_id = p.product_id
GROUP BY 1;
-- Result: Male 686 orders, Female 514 [cite: 160-178]

-- -----------------------------------------------
-- Q14. Purchasing behavior over time since account creation.
-- -----------------------------------------------
SELECT dc.customer_id AS "Customer ID", dc.full_name AS "Customer Name",
CASE
    WHEN (TO_DATE(fo.order_date, 'MM/DD/YYYY') - TO_DATE(dc.created_at, 'MM/DD/YYYY')) <= 30 THEN '0-30 days'
    WHEN (TO_DATE(fo.order_date, 'MM/DD/YYYY') - TO_DATE(dc.created_at, 'MM/DD/YYYY')) <= 60 THEN '31-60 days'
    ELSE '60+ days'
END AS "Period",
COUNT(DISTINCT fo.order_id) AS "Total Orders", SUM(foi.quantity) AS "Total Items"
FROM public."FactOrders" fo
JOIN public."DimCustomers" dc ON fo.customer_id = dc.customer_id
JOIN public."FactOrderItems" foi ON fo.order_id = foi.order_id
GROUP BY 1, 2, 3
ORDER BY 1, 2, 3;
-- Result: Varied across periods [cite: 179-203]

-- -----------------------------------------------
-- Q15. Customers who ordered in October but not December.
-- -----------------------------------------------
SELECT DISTINCT c.customer_id AS "Customer ID", c.full_name AS "Customer Name", c.city AS "Customer City"
FROM public."DimCustomers" c
WHERE c.customer_id IN (SELECT customer_id FROM public."FactOrders" WHERE EXTRACT(MONTH FROM TO_DATE(order_date, 'MM/DD/YYYY')) = 10)
AND c.customer_id NOT IN (SELECT customer_id FROM public."FactOrders" WHERE EXTRACT(MONTH FROM TO_DATE(order_date, 'MM/DD/YYYY')) = 12);
-- Result: 17 customers [cite: 204-218]

